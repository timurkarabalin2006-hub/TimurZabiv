# Запрашиваем ввод
a = int(input("Enter first number: "))
b = int(input("Enter second number: "))

# Операции
print(f"{a} + {b} = {a + b}")
print(f"{a} - {b} = {a - b}")
print(f"{a} * {b} = {a * b}")
print(f"{a} / {b} = {a / b}")   # обычное деление
print(f"{a} // {b} = {a // b}") # целочисленное деление
print(f"{a} % {b} = {a % b}")   # остаток от деления
print(f"{a}^2 = {a ** 2}")      # возведение в квадрат
print(f"{b}^2 = {b ** 2}")